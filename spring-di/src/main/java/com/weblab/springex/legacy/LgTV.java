package com.weblab.springex.legacy;

public class LgTV {
	public void turnOn() {
		System.out.println("LG-TV - 전원을 켜다");
	}
	
	public void turnOff() {
		System.out.println("LG-TV - 전원을 끄다");
	}
	
	public void soundUp() {
		System.out.println("LG-TV - 소리를 높이다");
	}
	
	public void soundDown() {
		System.out.println("LG-TV - 소리를 낮추다");
	}
}
